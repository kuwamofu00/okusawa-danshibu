              <p class="card-text">
文永9年（1272年）2月、日蓮大聖人が佐渡国（新潟県佐渡島）塚原でしたためられ、同じく佐渡に流罪中の最蓮房［さいれんぼう］に与えられた書とされる（1336㌻）。
もとは天台宗の学僧で、佐渡で大聖人の門下となった最蓮房が、生死一大事血脈という成仏の要諦に関する法門について質問したのに対して、回答している。
生死の苦悩を超克し成仏を実現させる妙法が、師から弟子に伝えられていくこと。「生死一大事血脈抄」では、
